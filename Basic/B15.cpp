#include <iostream>
using namespace std;

int main() {
    int x = 10;
    int y = 3;

    cout << (x + y) << "\n";
    cout << (x - y) << "\n";
    cout << (x * y) << "\n";
    cout << (x / y) << "\n";
    cout << (x % y) << "\n";

    int z = 5;
    ++z;
    cout << z << "\n";

    --z;
    cout << z << "\n";

    return 0;
}