#include <iostream>
#include <cmath>
using namespace std;

int main() {
    double x = 9.0, y = 2.0;

    cout << "sqrt(9) = " << sqrt(x) << endl;
    cout << "pow(9,2) = " << pow(x, y) << endl;
    cout << "sin(0) = " << sin(0) << endl;
    cout << "log(100) = " << log(100) << endl;
    cout << "ceil(4.3) = " << ceil(4.3) << endl;
    cout << "fabs(-8.5) = " << fabs(-8.5) << endl;

    return 0;
}